package com.JAVa;

public class Arrays {
    public static void main(String[] args) {
        // array function
        int marks[] = new int[5];
        marks[0] = 100;
        marks[1] = 99;
        marks[2] = 98;
        marks[3] = 90;
        marks[4] = 88;

        System.out.println(marks[0]);
        System.out.println(marks[1]);
        System.out.println(marks[2]);
        System.out.println(marks[3]);
        System.out.println(marks[4]);
    }
}
